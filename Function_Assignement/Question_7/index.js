// Write a function findSquare() returns the square of the input number which is passed as an argument in the function call.
// Function to find the square of a number
function findSquare(number) {
    return number * number; // Return the square of the input number
}

// Example usage
let inputNumber = 5; 
let result = findSquare(inputNumber);

console.log("The square of " + inputNumber + " is: " + result);
